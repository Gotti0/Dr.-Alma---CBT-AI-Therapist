export const cognitiveDistortions = `
인지 왜곡(Cognitive Distortions) 10가지:
1. 흑백 논리 (All-or-Nothing Thinking): 상황을 이분법적 범주로만 인식함. (예: 완벽하지 않으면 실패다)
2. 과잉 일반화 (Overgeneralization): 단일 부정적 사건을 영구적인 패턴으로 해석함. (예: 항상 이렇다)
3. 정신적 여과 (Mental Filtering): 긍정적인 요소는 배제하고 부정적인 세부 사항에만 집착함.
4. 긍정 격하 (Discounting the Positive): 긍정적인 경험이나 성취를 우연이나 무가치한 것으로 치부함.
5. 임의적 추론 (Jumping to Conclusions):
   - 독심술: 증거 없이 타인의 부정적 생각을 확신함.
   - 예언자적 오류: 미래의 부정적 결과를 기정사실화함.
6. 확대/축소 (Magnification/Minimization): 자신의 실수는 과장하고 타인의 성취나 자신의 장점은 축소함.
7. 감정적 추론 (Emotional Reasoning): 감정을 객관적 사실의 증거로 삼음. (예: 불안한 걸 보니 나쁜 일이 생길 거야)
8. 당위적 진술 (Should Statements): 자신이나 타인에게 비현실적인 규칙을 강요함. (예: ~해야만 한다)
9. 낙인찍기 (Labeling): 구체적 행동 대신 부정적 명사로 자신/타인을 정의함. (예: 나는 패배자야)
10. 자기 탓하기 (Personalization): 통제 불가능한 것에 책임감을 느낌.
`;

